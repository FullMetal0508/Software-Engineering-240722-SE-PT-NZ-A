// this function returns the sum of two numbers
function sum(a, b)
{
    return(a+b)
}

// this function returns substraction of one number from another
function subtract(a, b)
{
    return(a-b)
}

//this function multiplies one value by another
function multiply(a, b)
{
    return(a*b)
}

//this function divides one value from another
function divide(a, b)
{
    return(a/b)
}

console.log(sum(3, 4))
console.log(subtract(10, 5))
console.log(multiply(10,5))
console.log(divide(10,5))